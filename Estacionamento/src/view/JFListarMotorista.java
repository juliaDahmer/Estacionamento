        if(jTMotorista.getSelectedRow() != -1){
            int opcao = JOptionPane.showConfirmDialog(null, "Deseja exluir a vaga selecionada?", "Exclus√£o", JOptionPane.YES_NO_OPTION);
        if(opcao == 0) {
                MotoristaDAO dao = new MotoristaDAO();
                Motorista m = new Motorista();
                m.setIdVaga((int)jTMotoristaa.getValueAt(jTMotorista.getSelectedRow(), 0));
                dao.delete(m);
            }
        }else {
            JOptionPane.showMessageDialog(null, "Selecione uma vaga!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        readJTable();
